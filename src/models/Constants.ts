export function returnDate(dateString: string) {
  let dateArray = dateString.split('-');
  let now: Date = new Date();
  now.setFullYear(Number(dateArray[0]));
  now.setMonth(Number(dateArray[1]));
  now.setDate(Number(dateArray[2]));
  // now = {year: , month: Number(dateArray[1]), day: Number(dateArray[2])};
  return now;
}

